function lerDadosAluno() {
    var matricula = prompt("Digite a matrícula do aluno:");
    var nome = prompt("Digite o nome do aluno:");
    var sobrenome = prompt("Digite o sobrenome do aluno:");
    var cpf = prompt("Digite o CPF do aluno:");
    var sexo = prompt("Digite o sexo do aluno:");
    var dataNascimento = prompt("Digite a data de nascimento do aluno:");
    var endereco = prompt("Digite o endereço do aluno:");
    var telefone = prompt("Digite o telefone do aluno:");

    console.log("Matrícula:", matricula);
    console.log("Nome:", nome);
    console.log("Sobrenome:", sobrenome);
    console.log("CPF:", cpf);
    console.log("Sexo:", sexo);
    console.log("Data de Nascimento:", dataNascimento);
    console.log("Endereço:", endereco);
    console.log("Telefone:", telefone);
}

lerDadosAluno();
